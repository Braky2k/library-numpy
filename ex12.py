# Adicione um valor constante a todos os elementos de um vetor. Exemplo: Seja vetor = np.array([1, 2, 3, 4]) e constante = 5, o resultado seria array([6, 7, 8, 9])

import numpy as np

# Criando array
vetor = np.array([1, 2, 3, 4])
# Criando constante
constante = 5
# Soma da array com a constante 5
soma = vetor + constante
# Imprimindo
print(soma)
