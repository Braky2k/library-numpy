# Calcule a raiz quadrada de todos os elementos de um vetor. Exemplo: Seja vetor = np.array([4, 9, 16, 25]), o resultado seria array([2., 3., 4., 5.])

import numpy as np

# Criando array
vetor = np.array([4, 9, 16, 25])
# np.sqrt() calcula a raiz quadrada dos valores individuais da array.
raiz = np.sqrt(vetor)
# Imprimindo
print(raiz)
